<?
#demonstrate increment and decrement operator

$x = 10;
echo "++$x is " ,(++$x), "<br>";
echo "now x is ",$x,"<br>";

$x = 10;
echo "$x++ is " ,($x++), "<br>";
echo "now x is ",$x,"<br>";
$x = 10;
echo "--$x is " ,(--$x), "<br>";
echo "now x is ",$x,"<br>";
$x = 10;
echo "$x-- is " ,($x--), "<br>";
echo "now x is ",$x,"<br>";
?>